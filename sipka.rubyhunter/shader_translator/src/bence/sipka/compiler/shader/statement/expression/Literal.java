package bence.sipka.compiler.shader.statement.expression;

import bence.sipka.compiler.shader.elements.TypeDeclaration;

public abstract class Literal extends ExpressionStatement {
	private static final long serialVersionUID = 1L;

	private String value;

	public Literal(String value, TypeDeclaration type) {
		super(type);
		this.value = value;
	}

	public final String getValue() {
		return value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Literal other = (Literal) obj;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}

}
