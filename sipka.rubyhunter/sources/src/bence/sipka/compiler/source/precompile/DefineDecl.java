package bence.sipka.compiler.source.precompile;

import java.io.IOException;
import java.io.OutputStream;

import bence.sipka.compiler.source.SourceWritable;

public class DefineDecl implements SourceWritable {
	private final String expr;
	private final String value;

	public DefineDecl(String expr, String value) {
		this.expr = expr;
		this.value = value;
	}

	@Override
	public void write(OutputStream out) throws IOException {
		out.write(("#define " + expr + " " + value + "\n").getBytes());
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((expr == null) ? 0 : expr.hashCode());
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		DefineDecl other = (DefineDecl) obj;
		if (expr == null) {
			if (other.expr != null)
				return false;
		} else if (!expr.equals(other.expr))
			return false;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}

}
